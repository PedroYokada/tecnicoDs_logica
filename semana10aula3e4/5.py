# Inicializa a variável 'numero' com 1, para começar a contagem
numero = 1

# Loop while: vai executar enquanto 'numero' for menor ou igual a 10
while numero <= 10:
    # Imprime o número atual na sequência
    print(numero)
    
    # Incrementa 'numero' para passar para o próximo número
    numero += 1
